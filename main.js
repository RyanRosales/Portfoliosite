// Crunchyroll
$(document).ready(function(){
  $(window).scroll(function(){
      if($(window).scrollTop()>300){
    $('.crunchy-top-bttn').fadeIn(250);
}
else{
    $('.crunchy-top-bttn').fadeOut(250);
}
  });
  $('.crunchy-top-bttn').click(function(){
$('html,body').animate(
    {scrollTop:0},400
);
  });
});


